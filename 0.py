# primitive types in Python
age = 25              # int
grade = 'A'           # char (actually just a string of length 1 in Python)
is_passed = True      # boolean
temperature = 36.6    # float (no need for 'f' like in Java)

print("Age:", age)
print("Grade:", grade)
print("Passed:", is_passed)
print("Body Temperature:", temperature)
print()

# non-primitive types in Python
name = "Alice"                # string
scores = [90, 85, 78]         # list (similar to array in Java)
    
print("Name:", name)
print("Scores:")
for score in scores:
    print(score)
